# Внешний цикл для перебора чисел от 1 до 9
for i in range(1, 10):
    # Внутренний цикл для умножения чисел от 1 до 9 на текущее число во внешнем цикле
    for j in range(1, 10):
        # Вывод произведения чисел с разделителем (табуляцией или пробелом) без перевода строки
        print(f"{i} * {j} = {i * j}\t", end="")
    # Перевод строки после каждой строки таблицы
    print()
