# Введите непустую последовательность символов
sequence = input("Введите непустую последовательность символов: ")

# Создадим пустое множество для хранения знаков арифметических операций
operators_set = set()

# Знаки арифметических операций
arithmetic_operators = ['+', '-', '*', '/']

# Пройдемся по каждому символу в последовательности
for char in sequence:
    # Проверим, является ли символ знаком арифметической операции
    if char in arithmetic_operators:
        # Если да, добавим его в множество
        operators_set.add(char)

# Выведем результат
print("Множество знаков арифметических операций, встречающихся в последовательности:", operators_set)
