# Считываем число k с клавиатуры
k = int(input("Введите число k: "))

# Используем цикл с переменной для перебора чисел от 0 до k (включительно)
for num in range(k + 1):
    # Вычисляем квадрат и куб числа
    square = num ** 2
    cube = num ** 3
    # Выводим результат
    print(f"Корень числа {num} равен {square}, а куб равен {cube}")
