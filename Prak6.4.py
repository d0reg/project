# Инициализируем переменную для хранения суммы кубов нечетных чисел
sum_of_cubes = 0

# Используем цикл с переменной для перебора нечетных чисел от 7 до 37
for num in range(7, 38, 2):
    # Возводим каждое нечетное число в куб и добавляем его к сумме
    sum_of_cubes += num ** 3

# Выводим сумму кубов нечетных чисел
print(f"Сумма кубов нечетных чисел от 7 до 37: {sum_of_cubes}")
