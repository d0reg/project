def process_lists(list1, a, b, c):
    # Инициализируем переменную для хранения суммы элементов списка
    sum_of_others = 0

    # Итерируемся по элементам первого списка
    for num in list1:
        # Проверяем условия: больше a, меньше b, делится на c без остатка
        if num > a and num < b and num % c == 0:
            print(num)  # Выводим элементы, удовлетворяющие условиям
        else:
            sum_of_others += num  # Суммируем остальные элементы

    # Выводим сумму остальных элементов
    print("Сумма остальных элементов списка:", sum_of_others)


# Основная функция
def main():
    # Произвольный список целых чисел
    list1 = [5, 10, 15, 20, 25, 30, 35, 40]

    # Числа a, b, c
    a = 10
    b = 30
    c = 5

    # Вызываем функцию для обработки списков
    process_lists(list1, a, b, c)


# Вызов основной функции
main()
