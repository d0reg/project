# Запрос ввода строки с английским текстом
input_string = input("Введите строку с английским текстом: ")

# Разделение строки на слова
words = input_string.split()

# Инициализация счетчика слов, начинающихся с буквы "b"
b_words_count = 0

# Подсчет количества слов, начинающихся с буквы "b"
for word in words:
    # Проверяем, начинается ли слово с буквы "b" (регистронезависимо)
    if word.lower().startswith('b'):
        b_words_count += 1

# Вывод результата
print("Количество слов, начинающихся с буквы 'b':", b_words_count)
