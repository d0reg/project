import math


# Функция для расчета площади круга
def S(r):
    return math.pi * r ** 2


# Функция для расчета длины окружности
def l(r):
    return 2 * math.pi * r


# Функция для ввода радиуса и вывода площади и длины окружности
def krug():
    # Запрос радиуса у пользователя
    radius = float(input("Введите радиус окружности: "))

    # Вызов функций S и l для расчета площади и длины окружности
    area = S(radius)
    circumference = l(radius)

    # Вывод результатов
    print("Площадь круга:", area)
    print("Длина окружности:", circumference)


# Вызов функции для демонстрации
krug()
